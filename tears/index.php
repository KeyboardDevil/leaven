    <h1>Tears for Beers</h1>
    <p>
      <strong>O Grain! O Hops! O Water! O Yeast!</strong><br/>
      I miss thy concoction, the joy – it has ceased!<br/>
      A Hef, A Stout, A Porter, an Ale,<br/>
      Imbibing your glory has made me a whale.<br/>
      Pour me a pint, or hell, pour a vat,<br/>
      This diet is torment, I’d rather be fat.<br/>
      This life is now pointless, and so full of dread,<br/>
      Unless thee delivers me something with head.<br/>
      A bottle, a growler, A nitro on tap,<br/>
      I’d even take Bud, though I know it is crap.<br/>
      Domestic, imported, a finely tuned craft,<br/>
      The variance mocks me, by bottle or draught.<br/>
      My mood is despondent, I think you’ll agree,<br/>
      That I need a pour with a strong ABV.<br/>
      So bitter and sour I stand here today,<br/>
      Ironically like a great IPA.<br/>
      So I’m gonna drink, for I know what is right!<br/>
      Go tap the kegs, and line up a flight.<br/>
      To err is human, forgiveness divine,<br/>
      Life is a joke with a sad, empty stein.<br/>
    </p>
